import mongoose from 'mongoose'

const AdminUserSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true
  },
  name: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  lastLogin: {
    type: Date
  }
})

export const AdminUser = mongoose.models.AdminUser || mongoose.model('AdminUser', AdminUserSchema) 